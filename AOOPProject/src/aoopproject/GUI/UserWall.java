    public UserWall(User user) {
        initComponents();
        usernameLabel.setText(user.toString() + "'s Wall");
        friendsLabel.setText(user.toString() + "'s Friends");
        
        listModel = new DefaultListModel();
        friendsList.setModel(listModel);
        
        setFriendsList(user.getUsername());
        loadPostsToWall(user.getUsername());
    }
    
    private void setFriendsList(String username) {
        List<String> friends = FriendManager.readFriendsFromFile(username);
        
        listModel.clear();
        if (!friends.isEmpty()) {
            for (String user : friends) {
                listModel.addElement(user);
            }
        }
    }
    
    private void loadPostsToWall(String username) {
        List<Post> posts = WallManager.readPostsFromFile(username);
        
        jTextArea1.setText("");
        for (Post post : posts) {
            jTextArea1.append(post.getContent() + System.lineSeparator());
        }
    }

